#include "libft.h"

int	ft_isalnum(int a)
{
	while (a != '\0')
	{
		if (a >= 48 && a <= 57)
			return (1);
		else if (a >= 65 && a <= 90)
			return (1);
		else if (a >= 97 && a <= 122)
			return (1);
		else
			return (0);
	}
	return (0);
}
